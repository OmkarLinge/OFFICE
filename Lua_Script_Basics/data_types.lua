-- data_types.lua
-- Demonstrates basic Lua data types

local name = "Omkar"     -- string
local age = 21           -- number
local isStudent = true   -- boolean
local location = nil     -- empty value

print("Name:", name)
print("Age:", age)
print("Student:", isStudent)
print("Location:", location)

-- Explanation :- 
-- Lua mainly uses:
--                  String → text
--                  Number → numeric value
--                  Boolean → true/false
--                  Nil → no value
